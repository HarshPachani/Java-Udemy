package com.Udemy;

public class JoiningString {
    public static void main(String[] args) {
        String sentance = "My Name is ";
        String placeHolder = "Harsh";
        System.out.println(sentance + placeHolder);

        int point = 50;
        String announcement = "Points for Harsh";
        System.out.println(point + announcement );
    }
}
