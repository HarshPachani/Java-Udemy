package com.Udemy.Module_1.Loops_5;

public class N_15_NestedLoops {
    public static void main(String[] args) {
        for (int i=0 ; i < 3 ; i++){
            System.out.println("run: " + i);
            for (int j = 0; j < 3 ; j++){
                System.out.println(j);
            }
        }
    }
}
